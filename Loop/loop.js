// A loop is a programming construct that allows you to execute a block of code multiple times. It helps automate repetitive tasks.
// There are several types of loop: while, do while, for.

//while loop
var x=1;
while(x<=10)
{
console.log(x);
x++;
}

//for loop
for(j=1;j<=5;j++)
{
console.log(j);
}

//do while
i=1;
do
{
console.log(i);
i++;
}while(i<=7)


